package main

import "fmt"

func main() {

	// Declare a variable of type string set to its zero value.
	var name string

	// ''
	// Display the value of the variable.
	fmt.Println(name)

	// Declare a variable of type int set to its zero value.
	var age int

	// 0
	// Display the value of the variable.
	fmt.Println(age)

	// Declare a variable of type bool set to its zero value.
	var isCool bool

	// false
	// Display the value of the variable.
	fmt.Println(isCool)

	// Declare variables using the short variable declaration operator.
	// Declare a variable of type string and init using a literal.
	email := "tt@example.com"

	fmt.Println(email)
}
